using UnityEngine;
using System.Collections;

namespace STB.Basics
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>
    /// Class: BonesLateScaler
    /// # 
    /// </summary>
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public class BonesLateScaler : MonoBehaviour
    {
        // public 
        public float scaleMultiplier = 1;

        // private
        Vector3 baseScale = Vector3.one;


        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>
        /// Start
        /// </summary>
        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        void Start()
        {
            baseScale = this.transform.localScale;
        }
        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>
        /// LateUpdate
        /// </summary>
        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        void LateUpdate()
        {
            this.transform.localScale = scaleMultiplier * baseScale;
        }
    }
}
