using UnityEngine;
using System.Collections;
using System.Collections.Generic;
using UnityEngine.UI;

namespace STB.Basics
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>
    /// CLASS NAME: GuiImageRotator
    /// </summary>
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public class GuiImageRotator : MonoBehaviour
    {
        // public
        public Vector3 rotationVector = new Vector3(0, 0, 90);


        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>
        /// Update
        /// </summary>
        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        void Update()
        {
            this.gameObject.GetComponent<RectTransform>().Rotate(rotationVector * Time.fixedDeltaTime);
        }
    }
}