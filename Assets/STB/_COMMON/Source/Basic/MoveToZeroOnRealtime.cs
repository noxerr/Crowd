using UnityEngine;
using System.Collections;
using System.Collections.Generic;
using System.Linq;

namespace STB.Basics
{ 
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>
    /// Class: MoveToZeroOnRealtime
    /// </summary>
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public class MoveToZeroOnRealtime : MonoBehaviour
    {
        // public
        public bool localScale = true;


        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>
        /// Awake
        /// </summary>
        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        void Awake()
        {
            if (localScale) this.transform.localPosition = Vector3.zero;
            else this.transform.position = Vector3.zero;

            this.enabled = false;
        }
    }
}
