using UnityEngine;
using System.Collections;

namespace STB.Basics
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>
    /// Class: WeAreTheMainCamera
    /// # 
    /// </summary>
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public class WeAreTheMainCamera : MonoBehaviour
    {
        // public static
        public static Camera primaryCamera = null;

        // private
        static Camera realCamera = null;


        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>
        /// Awake
        /// </summary>
        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        void Awake()
        {
            realCamera = GetComponent<Camera>();

            //Debug.Log("realCamera name: " + realCamera.name);
        }
        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>
        /// RealCamera
        /// </summary>
        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        public static Camera RealCamera
        {
            get
            {
                if (!realCamera) return Camera.main;

                realCamera.GetComponent<Camera>().enabled = primaryCamera;

                if (primaryCamera) return primaryCamera;

                return realCamera;
            }
        }
        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>
        /// OnDestroy
        /// </summary>
        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        void OnDestroy()
        {
            realCamera = null;
            primaryCamera = null;
        }
    }
}
