using UnityEngine;
using System.Collections;
using System.Collections.Generic;

namespace STB.Generic
{ 
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>
    /// Class: GenericRotator
    /// # 
    /// </summary>
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public class GenericRotator : MonoBehaviour
    {
        // public
        public Vector3 rotationVector = Vector3.zero;
        public float maxRandomFactor = 1;
        public float multipler = 1;

        // private
        float actualRandomFactor = 0;


        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>
        /// Start
        /// </summary>
        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        void Start()
        {
            actualRandomFactor = Random.Range(1, maxRandomFactor);
        }
        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>
        /// Update
        /// </summary>
        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        void Update()
        {
            this.transform.Rotate(actualRandomFactor * multipler * rotationVector * Time.deltaTime);
        }
    }
}
