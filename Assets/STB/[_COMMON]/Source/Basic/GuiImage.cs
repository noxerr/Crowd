using UnityEngine;
using System.Collections;
using System.Collections.Generic;
using UnityEngine.UI;

namespace STB.Basics
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>
    /// CLASS NAME: GuiImage
    /// # To handle 2D images using canvas
    /// </summary>
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public class GuiImage : GuiGeneric
    {
        // private
        Image imageComponent = null;
        RawImage rawImageComponent = null;


        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>
        /// StartExtended -- OVERRIDED
        /// </summary>
        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        protected override void StartExtended()
        {
            imageComponent = this.GetComponent<Image>();

            rawImageComponent = this.GetComponent<RawImage>();
        }
        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>
        /// ImageComponent
        /// </summary>
        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        public Image GetImageComponent()
        {
            return imageComponent;
        }
        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>
        /// UpdateFading
        /// </summary>
        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        void UpdateFading()
        {
            if (imageComponent)
            {
                // fading
                Color actualColor = imageComponent.color;
                actualColor.a = Fading;
                imageComponent.color = actualColor;
            }

            if (rawImageComponent)
            {
                // fading
                Color actualColor = rawImageComponent.color;
                actualColor.a = Fading;
                rawImageComponent.color = actualColor;
            }
        }
        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>
        /// UpdateExtended -- OVERRRIDED
        /// </summary>
            ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        protected override void UpdateExtended()
        {
            UpdateFading();
        }
    }
}