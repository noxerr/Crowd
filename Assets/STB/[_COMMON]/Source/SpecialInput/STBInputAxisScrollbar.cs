using System;
using UnityEngine;

namespace STB.SpecialInput
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>
    /// Class: STBInputAxisScrollbar
    /// </summary>
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public class STBInputAxisScrollbar : MonoBehaviour
    {
        // public
        public string axis;


        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>
        /// HandleInput
        /// </summary>
        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        public void HandleInput(float value)
        {
            STBCrossPlatformInputManager.SetAxis(axis, (value*2f) - 1f);
        }
    }
}
