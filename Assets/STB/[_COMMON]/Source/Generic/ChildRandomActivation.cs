using UnityEngine;
using System.Collections;
using System.Collections.Generic;

namespace STB.Generic
{ 
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>
    /// Class: ChildRandomActivation
    /// # 
    /// </summary>
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public class ChildRandomActivation : MonoBehaviour
    {
        // public
        public float activationProbability = 50;


        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>
        /// Awake
        /// </summary>
        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        void Awake()
        {
            foreach (Transform t in this.transform)
            {
                t.gameObject.SetActive(Random.Range(0, 100) <= activationProbability);
            }
        }
    }
}
