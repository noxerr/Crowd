using UnityEngine;
using System.Collections;
using System.Collections.Generic;

namespace STB.Generic
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>
    /// Class: GenericGameObjectActivator
    /// # 
    /// </summary>
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public class GenericGameObjectActivator : MonoBehaviour
    {
        // public
        public List<GameObject> gameobjectsToActivateList = new List<GameObject>();

        ///////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>
        /// Awake
        /// </summary>
        ///////////////////////////////////////////////////////////////////////////////////////////////////////
        void Awake()
        {
            for (int i = 0; i < gameobjectsToActivateList.Count; i++)
            {
                gameobjectsToActivateList[i].SetActive(false);
            }
        }
        ///////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>
        /// Update
        /// </summary>
        ///////////////////////////////////////////////////////////////////////////////////////////////////////
        void Update()
        {
            for (int i = 0; i < gameobjectsToActivateList.Count; i++)
            {
                gameobjectsToActivateList[i].SetActive(Input.GetKey(KeyCode.F));
            }
        }
    }
}
