using UnityEngine;
using System.Collections;
using System.Collections.Generic;
 
namespace STB.PACS 
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>
    /// Class: IgnorePedestriansColliderAdder
    /// # 
    /// </summary>
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public class IgnorePedestriansColliderAdder : MonoBehaviour
    {
        // private
        bool doIt = true;

        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>
        /// Update
        /// </summary>
        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        void Update()
        {
            if (doIt)
            {
                doIt = false;

                BasicFunctions.AddToChildsIgnoreCollisionsWith(this.transform);
            }
        }
    }
}
 
