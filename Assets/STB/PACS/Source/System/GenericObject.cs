using UnityEngine;
using System.Collections;

namespace STB.PACS
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>
    /// Class: GenericObject
    /// # Handles the base activation of everything in the system depending on given parameters
    /// </summary>
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public class GenericObject : Generic.GenericObjectBase
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>
        /// StartExtended -- OVERRIDE
        /// </summary>
        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        protected override void StartExtended()
        {
            if (IndividualHandler.Singleton) IndividualHandler.Singleton.genericObjectList.Add(this);
            else Debug.Log("Warning: There is no IndividualHandler in the scene!");
        }
        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>
        /// GetWorkingDistance -- OVERRIDE
        /// </summary>
        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        public override float GetWorkingDistance()
        {
            if (IndividualHandler.Singleton && (manualWorkingDistance == 0)) return IndividualHandler.Singleton.workingDistance;

            return manualWorkingDistance;
        }
    }
}
